package com.cebolao.lotofacil.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Objeto para centralizar as dimensões de espaçamento usadas no aplicativo,
 * garantindo consistência visual e facilitando a manutenção.
 */
object Padding {
    /** Espaçamento padrão para o conteúdo principal da tela em relação às bordas. (20.dp) */
    val Screen = 20.dp

    /** Espaçamento grande, usado para separar seções importantes. (24.dp) */
    val Large = 24.dp

    /** Espaçamento padrão dentro de cards e entre componentes. (16.dp) */
    val Card = 16.dp

    /** Espaçamento médio, para agrupar elementos relacionados. (12.dp) */
    val Medium = 12.dp

    /** Espaçamento pequeno, entre itens como texto e ícone. (8.dp) */
    val Small = 8.dp

    /** Espaçamento mínimo, para ajustes finos. (4.dp) */
    val ExtraSmall = 4.dp
}